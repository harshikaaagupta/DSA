
public class operators {
    public static void main(String args[]){
        //Bitwise AND &
        System.out.println((5 & 6));
        //Bitwise OR |
        System.out.println((5|6));
        //Bitwise XOR
        System.out.println((5^6));
        //Bitwise One's Complement ~
        System.out.println((~5));
        //Binary Left Shift <<
        System.out.println((5<<2));
        //Binary Right Shift >>
        System.out.println((6>>1));
    }
    
}
